# Descripció: El programa crea una funció que suma tots els nombres d'una llista.

def sumar_llista(numeros):
    return sum(numeros)

# Exemple d'ús
numeros = [2, 5, 8, 3, 6]
resultat = sumar_llista(numeros)
print("La suma de tots els nombres de la llista és:", resultat)
