# Descripció: El programa concatena dues cadenes separades per un guió "-".

cadena1 = input("Introdueix la primera cadena: ")
cadena2 = input("Introdueix la segona cadena: ")

# Concatena les dues cadenes amb un guió entre elles
resultat = cadena1 + "-" + cadena2

# Mostra el resultat
print("El resultat de la concatenació és:", resultat)
