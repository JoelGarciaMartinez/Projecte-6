# Demanar els tres números
num1 = int(input("Introdueix el primer número: "))
num2 = int(input("Introdueix el segon número: "))
num3 = int(input("Introdueix el tercer número: "))

# Trobar el número més gran
més_gran = max(num1, num2, num3)

# Mostrar el resultat
print("El número més gran és:", més_gran)
